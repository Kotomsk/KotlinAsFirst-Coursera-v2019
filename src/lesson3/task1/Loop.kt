 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */

fun countCount (n: Int): Int {
    var count = 1
    var nCount = n

    while (nCount / 10 != 0) {
        count += 1
        nCount = (nCount - nCount % 10) / 10
    }
    return count
}

fun fibSequenceDigit(n: Int): Int {

    var aFib = 0
    var sizeFib: Int
    var delta: Int
    var count = 0

    for (i in 1..n) {
        aFib = fib(i)
        sizeFib = countCount(fib(i))
        count = count + sizeFib


        if (count == n) {
            return aFib % 10
        } else if (count > n) {
            delta = count - n
            for (f in 1..delta) {
                aFib = (aFib - aFib % 10) / 10
            }
            break
        }
    }
    return aFib
}
